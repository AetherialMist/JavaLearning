package aetherial;

public class Calculator {

    public Calculator() {

    }

    public int mult(int x, int a) {
        x = x * a;
        return x;
    }

    public int div(int x, int y) {
        return x / y;
    }

    public int add(int x, int y) {
        return x + y;
    }

    public int sub(int x, int y) {
        return x - y;
    }

}